select *,case when receiver>sender then sender
else receiver end as senders,
case when receiver>sender then receiver
else sender end as receivers
from subscriber
)
select senders,receivers,sum(sms_no) as total_sms
from cte
group by  senders,receivers
order by sum(sms_no)
